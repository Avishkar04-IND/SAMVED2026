export function getLang() {
  return localStorage.getItem('safetynet_lang') || 'en';
}

export function setLang(code) {
  localStorage.setItem('safetynet_lang', code);
  // No reload — LangContext handles state update reactively
}

// ── Main translation hook ─────────────────────────────────────
// Usage: const { t } = useT('login');
//        t('title') → translated string
export function t(section, key, lang) {
  const l   = lang || getLang();
  const sec = translations[section];
  if (!sec) return key;
  const langData = sec[l] || sec['en'];
  return langData[key] || sec['en'][key] || key;
}